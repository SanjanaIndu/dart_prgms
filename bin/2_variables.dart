void main(){
   /// variable : name that we given to memory location where we store the value
  /// syntax:
  /// data_type variable_name = value;
  /// eg: String name = 'SANJANA';

  String fname ='Sanjana';
  String lname = 'S';
  // string interpolation  --->  $  or  ${}

  print('my name is $fname $lname');


  int a = 10, b = 20;
  print('sum = ${a+b}');

  /// $    --> single value
  /// ${}  --> to perform an operation with more than one value

}