/// Data type of expression and data type of case should be same

void main(){
  int size = 7;

  switch(size){
    case 4 :
      print('Size is 4');
    case 5 :
      print('Size is 5');
    case 6 :
      print('Size is 6');
    case 7 :
      print('Size is 7');
    case 8 :
      print('Size is 8');
    case 9 :
      print('Size is 9');
    default :
      print('Size unavailable');
  }
}