void main(){
  String size = 'L';
  if (size == 'XS'){
    print('size is XS');
  }else if(size == 'XL'){
    print('size is XL');
  }
  else if(size == 'S'){
    print('size is S');
  }
  else if(size == 'M'){
    print('size is M');
  }else if(size == 'L'){
    print('size is L');
  }
else{
  print('Size unavailable');
}
}